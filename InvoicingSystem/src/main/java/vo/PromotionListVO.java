package vo;

public class PromotionListVO {
	ListVO free;
	double money;
	int paper;
	int papernum;
	public PromotionListVO(ListVO free,double money,int paperedu,int papernum2){
		this.free = free;
		this.money = money;
		this.paper = paperedu;
		this.papernum = papernum2;
	}
	
	public int getNum(){
		return papernum;
	}
	
	public int getPaper(){
		return paper;
	}
	
	public double getMoney(){
		return money;
	} 
	
	public ListVO getList(){
		return free;
	}

}
