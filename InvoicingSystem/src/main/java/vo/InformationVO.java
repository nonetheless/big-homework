package vo;

public class InformationVO {
	String memberName;
	String memberId;
	String house;
	String user;
	String other;
	int kind;
	String saler;
	public InformationVO(String name,String id,String house,String user,String other,int kind,String saler){
		this.memberId = id;
		this.memberName = name;
		this.house = house;
		this.other = other;
		this.user = user;
		this.kind =kind;
		this.saler  = saler;
	}
	
	public String getId(){
		return this.memberId;
	}
	
	public String getName(){
		return this.memberName;
	}
	
	public String getHouse(){
		return this.house;
	}
	
	public String getOther(){
		return this.other;
	}
	
	public String getUser(){
		return this.user ;
	}
	
	public int getKind(){
		return this.kind;
	}

	public String getSaler() {
		
		return saler;
	}
	
	
	

}
