package vo;

public class ShowListVO {
	String id;
	String memberid;
	String membername;
	double price;
	public ShowListVO(String id,String memberid,String name,double p){
		this.id = id;
		this.memberid = memberid;
		this.membername = name;
		this.price = p;
	}
	
	public String getId(){
		return id;
	}
	
	public String getMId(){
		return memberid;
	}
	
	public String getMname(){
		return membername;
	}
	
	public double getPrice(){
		return price;
	}

}
