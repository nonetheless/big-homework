package vo;

public class MarketCommodityVO {
	String id;
	double in,out;
	int number;
	String name,kind;
	public MarketCommodityVO(String id,String name,String kind,int num,double in,double out){
		this.id = id;
		this.in = in;
		this.out  =out;
		this.number = num;
		this.kind = kind;
		this.name  = name;
	}
	public String getId(){
		return id;
	}
	
	public String getName(){
		return name;
	}
	
	public String getKind(){
		return kind;
	}
	
	public int getNum(){
		return number;
	}
	
	public double getIn(){
		return in;
	}
	
	public double getOut(){
		return out;
	}
	
	
	

}
