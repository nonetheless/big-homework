package po;

import java.io.Serializable;

public class MarketUniPO implements Serializable{
	/**
	 * 用于Market的自动编号
	 */
	private static final long serialVersionUID = 1L;
	String time;
	int salenum;
	int stacknum;
	int unsalenum;
	int unstacknum;
	int giftnum;
	
	public MarketUniPO(String time,int salenum,int stacknum,int unsalenum,int unstacknum,int giftnum){
		this.time = time;
		this.salenum = salenum;
		this.stacknum = stacknum;
		this.giftnum = giftnum;
		this.unsalenum = unsalenum;
		this.unstacknum = unstacknum;
	}
	public String getTime(){
		return time;
	}
	
	public int getSale(){
		return salenum;
	}
	
	public int getStack(){
		return stacknum;
	}
	
	public int getUnsale(){
		return unsalenum;
	}
	
	public int getUnstack(){
		return unstacknum;
	}
	
	public int getGift(){
		return giftnum;
	}

}
